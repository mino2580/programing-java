package learncode;

public class cykly_break_continue {

	public static void main(String[] args) {
		 for(int i=0;i<10;i++) {
		     if(i==5) {
			 System.out.println("break");
			 break;
		     }
		  System.out.println(i);
		  }
		 
		 //////////////////////////////
		 
		 System.out.println("\n//////////////////");
		 
		 for(int i=0;i<10;i++) {
		     if(i==5) {
			 System.out.println("continue");
			 continue;
		     }
		  System.out.println(i);
		  }
		 
		 
		 
		 
	}

}
